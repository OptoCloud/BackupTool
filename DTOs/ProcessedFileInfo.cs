namespace OptoPacker.DTOs;

public record struct ProcessedFileInfo(string Path, ulong Size, byte[] Hash, string? Error = null)
{
    private static readonly byte[] EmptyDataHash = new byte[] {
        0xE3, 0xB0, 0xC4, 0x42, 0x98, 0xFC, 0x1C, 0x14, 0x9A, 0xFB, 0xF4, 0xC8, 0x99, 0x6F, 0xB9, 0x24, 0x27, 0xAE, 0x41, 0xE4, 0x64, 0x9B, 0x93, 0x4C, 0xA4, 0x95, 0x99, 0x1B, 0x78, 0x52, 0xB8, 0x55
    };
    public static ProcessedFileInfo EmptyInfo(string path) => new(path, 0, EmptyDataHash);
    public static ProcessedFileInfo ErrorInfo(string path, ulong size, string error) => new(path, size, Array.Empty<byte>(), error);
}